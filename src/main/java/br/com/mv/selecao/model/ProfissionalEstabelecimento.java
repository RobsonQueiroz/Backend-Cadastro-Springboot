package br.com.mv.selecao.model;

public class ProfissionalEstabelecimento {
	private Long profissional_id;
	private Long estabelecimentos_id;
	
	public Long getProfissional_id() {
		return profissional_id;
	}
	public void setProfissional_id(Long profissional_id) {
		this.profissional_id = profissional_id;
	}
	public Long getEstabelecimentos_id() {
		return estabelecimentos_id;
	}
	public void setEstabelecimentos_id(Long estabelecimentos_id) {
		this.estabelecimentos_id = estabelecimentos_id;
	}
}
